import type { Question } from "./questions.js";

// CFA Level 1 multiple-choice questions.
// Format: exact-match evaluation (correctAnswer letter). No AI call.
// 10 topics × 10 questions. Mostly easy/medium with 1-2 tougher items per topic.

interface McqDef {
  id: string;
  type: string;
  title: string;
  prompt: string;           // question stem
  choices: string[];        // 4 options (A..D)
  correctAnswer: string;    // "A" | "B" | "C" | "D"
  explanation: string;      // shown after answer
  difficulty?: "easy" | "medium" | "hard";
}

function mcq(d: McqDef): Question {
  return {
    id: d.id,
    category: "cfa",
    type: d.type,
    difficulty: d.difficulty ?? "easy",
    title: d.title,
    prompt: d.prompt,
    diff: "",
    format: "mcq",
    choices: d.choices,
    correctAnswer: d.correctAnswer,
    explanation: d.explanation,
    // rubric is unused for MCQ (evaluation is exact-match), but the schema requires it.
    rubric: { mustCover: [], strongSignals: [], weakPatterns: [] },
  };
}

